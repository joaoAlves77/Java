public class App2 {
    public static void main(String[] args) throws Exception {
         for (int i = 1; i <= 10; i++){
            for (int j = 1; j <= 10; j++){
                System.out.println(j + "x" + i + " = " + j * i);
                // 1x1 = 1
                // 2x2 = 2
            }
             
         }
     }
}
